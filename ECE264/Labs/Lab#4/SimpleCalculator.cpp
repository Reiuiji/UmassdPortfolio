#include "SimpleCalculator.h"

//add function
double SimpleCalculator::addition( double a, double b)
{
    return a + b;
}

//sub function
double SimpleCalculator::subtract( double a, double b)
{
    return a - b;
}

//multiply function
double SimpleCalculator::multiply( double a, double b)
{
    return a*b;
}

//division function
double SimpleCalculator::division( double a, double b)
{
    return a/b;
}

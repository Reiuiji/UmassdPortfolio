Volume=8000;
%K = input('please enter K ([0.2:0.7])')
fprintf('\n\t|---------------|---------------|---------------|')
fprintf('\n\t|\t S \t|\t A \t|\t B\t|')
fprintf('\n\t|---------------|---------------|---------------|')
for K=0.2:0.1:0.7
    X=Volume*4/((K+1)*(1-K));
    B=X^(1/3);
    A=K*B;
    S=pi^2*(B^2-A^2);
    fprintf('\n\t|\t%5.2f\t|\t%5.2f\t|\t%5.2f\t|',S,A,B) 
end
fprintf('\n\t|_______________|_______________|_______________|\n\n')